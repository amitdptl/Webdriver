public class SumArray
{
    public static void main(String[] args)
    {
        int a[]={12,14,16,12,17};
        int sum=0;
        for (int i = 0; i <a.length ; i++)
        {
            sum=sum+a[i];
        }
        System.out.println(sum);

    }
}
